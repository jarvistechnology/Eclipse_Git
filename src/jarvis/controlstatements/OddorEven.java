package jarvis.controlstatements;

public class OddorEven {
	
	
	public static void main(String[] args) {
		
		
		for (int i = 1; i <=10; i++) {
			
			if (i%3!=0) {  //2%2 ->0 ==0
				
				System.out.println(i+" is Odd Number");
				
			}
			
			else {
				
				System.out.println(i+" is Even Number");
			}
			
		}
	}

}

